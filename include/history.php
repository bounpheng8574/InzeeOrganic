          <h2 class="mt-4">ປະຫວັດຄວາມເປັນມາ</h2>
          <p> <dd>   ຮ້ານອິນຊີບ້ານເຮົາ ເປັນຮ້ານທີ່ເປີດໃຫ້ບໍລິການ ຈໍາໜ່າຍສິນຄ້າ Organic ແລະ ສິນຄ້າODOP. ສະຖານທີ່ຕັ້ງຢູ່ ບ້ານສົມຫວັງເໜືອ, ເມືອງຫາດຊາຍຟອງ, ນະຄອນຫຼວງວຽງຈັນ. ຮ້ານອິນຊີບ້ານເຮົາ ເປັນຮ້ານຂອງກຸ່ມກະສິກໍາອິນຊີນະຄອນຫຼວງ ( ສ່ວນບຸກຄົນ ) ຂຶ້ນກັບກະຊວງກະສິກໍາ ແລະ ປ່າໄມ້, ໂດຍແມ່ນ <b>ທ່ານ ຄໍາພູ ພັນທະບູນ ເປັນປະທານ</b>. ຮ້ານອິນຊີບ້ານເຮົາໄດ້ເລີ່ມເປີດໃຫ້ບໍລິການຕັ້ງແຕ່ວັນທີ 01/04/2016 ເປັນຕົ້ນມາ. ສິນຄ້າທີ່ນໍາເຂົ້າແມ່ນມາຈາກຊາວກະສິກອນຂອງກຸ່ມກະສິກໍາອິນຊີນະຄອນຫຼວງ ເປັນຕົ້ນແມ່ນພືດຜັກ ແລະ ໝາກໄມ້ອິນຊີ <br/>(ບໍ່ມີການນໍາໃຊ້ສານເຄມີ ຫຼື ສານສັງເຄາະໃດໆ ເຂົ້າໃນການຜະລິດ), ເຊິ່ງສິນຄ້າກ່ອນທີ່ຈະນໍາເຂົ້າ ທາງຮ້ານແມ່ນໄດ້ມີການກວດສອບ ເພື່ອໃຫ້ໄດ້ສິນຄ້າທີ່ໄດ້ມາດຕະຖານອອກສູ່ສັງຄົມ. &nbsp;<button class="btn btn-primary butthov" type="button" data-toggle="collapse" data-target="#collapseExample" aria-expanded="false" aria-controls="collapseExample">
