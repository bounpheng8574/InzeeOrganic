   ຜ່ານການດໍາເນີນການບໍລິການ ແລະ ຈໍາໜ່າຍສິນຄ້າແຕ່ມື້ທໍາອິດມາເຖິງປະຈຸບັນ ເຫັນວ່າທາງຮ້ານແມ່ນມີລູກຄ້າໃຫ້ຄວາມສົນໃຈ ແລະ ເຂົ້າມາບໍລິການເພີ່ມຂຶ້ນ. ສະນັ້ນ, ຈາກສະພາບການດັ່ງກ່າວຈຶ່ງເຫັນວ່າມັນໄດ້ເຮັດໃຫ້ການຈັດເກັບຂໍ້ມູນສິນຄ້າມີຄວາມຫຍຸ້ງຍາກ ແລະ ບໍ່ເປັນລະບຽບເນື່ອງຈາກວ່າຂະບວນການເຮັດວຽກສ່ວນຫຼາຍແມ່ນຍັງໃຊ້ວິທີການຈົດກ່າຍໃສ່ປຶ້ມ ຫຼື ເຈ້ຍບັນຊີຕ່າງໆ, ນອກນັ້ນ ຮ້ານອິນຊີບ້ານເຮົາຍັງພົບກັບບັນຫາອີກຫຼາຍດ້ານເຊັ່ນ: ການເກັບຂໍ້ມູນຍັງບໍ່ເປັນລະບຽບ, ການໂຄສະນາ ແລະ ໂປໂມດສິນຄ້າຍັງບໍ່ທັນທົ່ວເຖິງ, ລູກຄ້າບໍ່ສາມາດສັ່ງຈອງສິນຄ້າຜ່ານເວັບໄດ້ ແລະ ເສຍເວລາເດີນທາງເພື່ອມາຊື້ສິນຄ້າ ດ້ວຍຕົວເອງ.
          <p>   ສະນັ້ນ, ພວກເຮົາຈິ່ງເຫັນໄດ້ເຖິງຄວາມສໍາຄັນຂອງບັນຫາດັ່ງກ່າວຈິ່ງມີຄວາມຕ້ອງການຢາກຈະສ້າງ ລະບົບຈັດການຂາຍສິນຄ້າ ຂອງຮ້ານອິນຊີບ້ານເຮົາຂຶ້ນມາ ເພື່ອສ້າງຄວາມຫຼາກຫຼາຍໃນດ້ານທຸລະກິດ ໂດຍສະເພາະກໍແມ່ນເພື່ອຕອບໂຈດໃຫ້ມີການນຳໃຊ້ເຄື່ອງມືເຕັກໂນໂລຊີຫຼາຍຂຶ້ນເພື່ອສະດວກໃນການຈັດເກັບຂໍ້ມູນ ແລະ ຫຼຸດຂັ້ນຕອນໃນການເຮັດວຽກຫຼາຍຂຶ້ນ. ພ້ອມກັນນັ້ນຍັງເປັນການເພີ່ມຄວາມໄວໃຫ້ການບໍລິການ ແລະ ສ້າງຄວາມພໍໃຈໃຫ້ກັບລູກຄ້າອີກດ້ວຍ.</p>
          
